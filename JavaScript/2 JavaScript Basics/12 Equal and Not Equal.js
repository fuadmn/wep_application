// Comparison Operators: 

let x = 10;
let y = "10";

// equal to
console.log(x == y); // Hubi haddii labada qiime ay siman yihiin.

// not equal to
console.log(x != y); ///Waxay hubisaa haddii laba qiime aanay sinnayn.

// Strictly equal
console.log(x === y); // Wuxuu hubiyaa in labada qiime ay siman yihiin oo ay isku nooc yihiin.

// Strictly not equal

console.log(x !== y); // Wuxuu hubiyaa in labada qiime aanay sinnayn oo aanay isku nooc ahayn.
