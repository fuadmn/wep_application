// Logical Operators:

// AND (&&): in la soo gudbo labo shey oo true
// OR (||):hii mid ka mid aa laga helo wayee soo celinee true
// NOT (!): waa cagsi haduu true yahay waxa u ka dhigaa false

let isAdult = true;
let hasPermission = false;

// AND &&

console.log(isAdult && hasPermission);

// or ||
console.log(isAdult || hasPermission);

// NOT (!):

console.log(!hasPermission);
