// Ternary Operator  //hal len in aad ku soo kobto if-else

// : //waxa laga wadaa if-else // hadii la waayo

let age = 18;

const message = age >= 18 ? "You're an adult." : "You're minor.";

console.log(message);

// And Operator  &&

if (age >= 18) {
  console.log("You're an adult.");
}

const me = age >= 18 && "You're an adult.";

console.log(me);
